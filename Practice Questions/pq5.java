// sum of two numbers using users input

import java.util.*;

public class pq5{
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int a = scn.nextInt();
        int b = scn.nextInt();

        int sum = a + b; 

        System.out.println(sum);

        scn.close();
    }
}  